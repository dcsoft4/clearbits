void CClearBitsDlg::DeleteFileFromPlaylist() 
{
    int nSel = m_lbWaveFiles.GetCurSel();
	if ( (m_lbWaveFiles.GetCount() > 0) && (nSel >= 0))	// selected file
	{
		BOOL bIsPlaying = m_WaveReader.IsOpen();
		Stop();
		
		// delete from listbox
		m_lbWaveFiles.DeleteString (nSel);
		
		// Save playlist
		SavePlaylist();
		
		if (nSel >= m_lbWaveFiles.GetCount())
			nSel = m_lbWaveFiles.GetCount();
		
		// Select next file in list
		if (nSel >= 0)	// valid selection
		{
			m_lbWaveFiles.SetCurSel (nSel);
			
			// Resume playback
			if (bIsPlaying)
				Play (TRUE);
		}
	}
	
	m_lbWaveFiles.SetFocus();
}

void CClearBitsDlg::DeleteFileFromDisk() 
{
    int nSel = m_lbWaveFiles.GetCurSel();
	if ( (m_lbWaveFiles.GetCount() > 0) && (nSel >= 0))	// selected file
	{
		CString strCurFile;
		m_lbWaveFiles.GetText (nSel, strCurFile);

		// DC HACK:  Remove number at beginning
		if (isdigit(strCurFile[0]))
		{
			int iDot = strCurFile.Find ('.');
			if ( (iDot >= 0) && (iDot <= 4) )
				strCurFile = strCurFile.Mid ( iDot + 2 );
		}

		CString strMsg;
		strMsg.Format ("Really delete file\n%s?", (LPCTSTR) strCurFile);
		if ( AfxMessageBox (strMsg, MB_YESNO | MB_ICONQUESTION) == IDYES )
		{
			// delete from playlist
			DeleteFileFromPlaylist();

			// delete from disk
			if ( !DeleteFile (strCurFile) )
			{
				strMsg.Format ("Could not delete\n%s", (LPCTSTR) strCurFile);
				AfxMessageBox (strMsg);
			}
		}
	}

	m_lbWaveFiles.SetFocus();
}


