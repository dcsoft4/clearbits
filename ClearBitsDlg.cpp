void CClearBitsDlg::OnButtonMove() 
{
	// Move the selected file
	int nSel = m_lbWaveFiles.GetCurSel();
	if ( (m_lbWaveFiles.GetCount() > 0) && (nSel >= 0))	// file is selected
	{
		// Ask for dest dir
		CBrowseDlg dlg (GetSafeHwnd(), _T("Destination folder"), COptions::m_strMoveDestDir);
		if ( dlg.DoModal() == IDOK )
		{
			// Save dest dir in COptions
			CString strDest = dlg.m_strDir;
			if ( strDest[strDest.GetLength() - 1] != '\\' )
				strDest += '\\';		// ensure strDest ends with "\" so we can append filename directly after it
			COptions::m_strMoveDestDir = strDest;


			// Get source file
			CString strCurFile;
			m_lbWaveFiles.GetText (nSel, strCurFile);

			// DC HACK:  Remove number at beginning
			if (isdigit(strCurFile[0]))
			{
				int iDot = strCurFile.Find ('.');
				if ( (iDot >= 0) && (iDot <= 4) )
					strCurFile = strCurFile.Mid ( iDot + 2 );
			}

			// Get dest file
			CFileName fnSource (strCurFile);
			strDest += fnSource.GetFile() + fnSource.GetExt();

			// Stop playback so file is not in use
			BOOL bIsPlaying = m_WaveReader.IsOpen();
			Stop();

			// move it
			if ( MoveFile (strCurFile, strDest) )
			{
				// delete from listbox
				m_lbWaveFiles.DeleteString (nSel);

				// Save playlist
				SavePlaylist();
			}
			else
			{
				CString strMsg;
				strMsg.Format ("Could not move\n%s to\n%s", (LPCTSTR) strCurFile, (LPCTSTR) strDest);
				AfxMessageBox (strMsg);
			}


			if (nSel >= m_lbWaveFiles.GetCount())
				nSel = m_lbWaveFiles.GetCount();

			if (nSel >= 0)	// valid selection
			{
				m_lbWaveFiles.SetCurSel (nSel);

				// Resume playback
				if (bIsPlaying)
					Play (TRUE);
			}
		}

		m_lbWaveFiles.SetFocus();
	}
}

