BOOL CClearBitsDlg::OnInitDialog()
{
	CDialog::OnInitDialog();

	// Add "About..." menu item to system menu.

	// IDM_ABOUTBOX must be in the system command range.
	ASSERT((IDM_ABOUTBOX & 0xFFF0) == IDM_ABOUTBOX);
	ASSERT(IDM_ABOUTBOX < 0xF000);

	CMenu* pSysMenu = GetSystemMenu(FALSE);
	if (pSysMenu != NULL)
	{
		CString strAboutMenu;
		strAboutMenu.LoadString(IDS_ABOUTBOX);
		if (!strAboutMenu.IsEmpty())
		{
			pSysMenu->AppendMenu(MF_SEPARATOR);
			pSysMenu->AppendMenu(MF_STRING, IDM_ABOUTBOX, strAboutMenu);
		}
	}

	// Set the icon for this dialog.  The framework does this automatically
	//  when the application's main window is not a dialog
	SetIcon(m_hIcon, TRUE);			// Set big icon
	SetIcon(m_hIcon, FALSE);		// Set small icon

	// 256 color logo	
	m_LogoDibWnd.SetDib (AfxGetResourceHandle(), IDB_LOGO, TRUE);
	VERIFY (m_LogoDibWnd.SubclassDlgItem (IDC_STATIC_LOGO, this));
	
	// Static text controls bolded
	LOGFONT lf;							   
	VERIFY (m_staticTitle.GetTextLogFont(&lf));
	lf.lfWeight = FW_BOLD;
	m_fontBold.CreateFontIndirect (&lf);
	VERIFY (m_staticTitle.SetTextFont(&m_fontBold));

	SetAlgo (ALGO_FIRST);

	DragAcceptFiles();

	m_WaveReader.Create();

	// Init buffer sizing
	// Seed the random number generator
	srand( (unsigned)time( NULL ) );
	
	VERIFY ( CryptAcquireContext(&m_hProvider, 0, 0, PROV_RSA_FULL, CRYPT_VERIFYCONTEXT) );
    if ( !m_hProvider )
        AfxMessageBox (_T("Warning:  Could not initialize ClearBits subsystem (internal error 1"));


	if ( !m_fileRandomData.LoadFromResource (AfxGetResourceHandle(), IDR_BIN_RANDOM_DATA, _T("BIN")) )
        AfxMessageBox (_T("Warning:  Could not initialize ClearBits subsystem (internal error 2"));


	// Prepare output device and buffers for playing
	OpenOutDevice(NULL);
	ASSERT (m_hWaveOut);
	int i;
	for (i=0; i < NUM_SAMPLE_BUFFERS; i++)
	{
		m_arrSB[i].Alloc (MAX_SOUNDBUF_SIZE);	
	}

	for (i=0; i < NUM_SAMPLE_BUFFERS; i++)
		PlayBuffer (&m_arrSB[i]);


	// Load window position from profile
	CWindowPlacement wp;
	wp.Restore( _T("WindowPos"), this );
	
	return TRUE;  // return TRUE  unless you set the focus to a control
}


